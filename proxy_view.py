def _plant_stacks(perm_buffs):
    """perm_buffs({BuffType键:层数}) → yisim {*_stacks字段:层数}(只留灵植项、值>0)。"""
    out = {}
    for k, field_name in LINGZHI_BUFF_TO_YISIM.items():
        v = (perm_buffs or {}).get(k)
        if v:
            out[field_name] = int(v)
    return out


def build_view_model(state, counter=None, last_battle=None, opp_tracker=None):
    sh = shadow_state.shadow
    unlocked = shadow_state.unlocked_board_slots(state.round_num)

    vm = {
        "round": state.round_num,
        "phase": state.phase,
        "me": None,
        "opponent": None,
        "counter": {"remaining": counter.remaining() if counter else {}},
    }

    # ── You ──────────────────────────────────────────────────────────────────
    if state.me_index >= 0 and state.me_index < len(state.players):
        me = state.players[state.me_index]
        # 灵羽 merge: lv2/3 灵羽 placed on the board merges into a lv1 qi/agility
        # target at battle-time (server-side, no wire signal). Substitute before
        # building the view-model so the UI/damage sim see the merged result.
        # Any 灵羽 lv2/3 that finds NO valid target stays as 灵羽 and is logged
        # in `lingyu_unresolved` so the damage panel can show "unidentified card".
        lingyu_unresolved = []
        if sh is not None:
            from lingyu_merge import resolve_lingyu_merges
            merged_board, lingyu_unresolved = resolve_lingyu_merges(list(sh.board))
            hand = [_card(c) for c in sh.hand if c is not None]
            board = _board_list(merged_board, unlocked)
            xiuwei, tipo, realm = sh.xiuwei, sh.tipo, sh.realm_tier
            rerolls = sh.rerolls
            seasonal = [_card(c) for c in sh.seasonal.values() if c is not None]
        else:
            from lingyu_merge import resolve_lingyu_merges
            merged_board, lingyu_unresolved = resolve_lingyu_merges(list(me.board or []))
            hand = [_card(c) for c in me.cards]
            board = _board_list(merged_board, unlocked)
            xiuwei, tipo, realm = me.xiuwei, me.tipo, me.realm_tier
            rerolls = getattr(me, "rerolls", 0)
            seasonal = []
        # HP / tipo / maxTipo prefer battle_log.json (authoritative). When BL
        # doesn't have a record for this round we fall back to the formula
        # `realm_base[realm] + (R-1)*2` and flag the value as `hpIsPredicted`
        # so the UI can render it visually distinct from authoritative values.
        # The wire does NOT carry HP directly.
        log_stats = _battle_log_stats(state.round_num, getattr(me, "display_name", ""))
        log_hp     = int(log_stats["max_hp"])     if log_stats else 0
        log_tipo   = int(log_stats["tipo"])       if log_stats else None
        log_maxtp  = int(log_stats["max_tipo"])   if log_stats else 0
        log_round  = int(log_stats["from_round"]) if log_stats else None

        hp_is_predicted = (log_hp <= 0)
        if hp_is_predicted:
            hp_val = _predict_hp(state.round_num, realm)
        else:
            hp_val = log_hp
        tipo_val = log_tipo if log_tipo is not None else tipo
        max_tipo = log_maxtp  # 0 if log missing — UI treats 0 as "unknown"

        avail, thr, gate = shadow_state.breakthrough_status(realm, xiuwei, tipo_val)
        fates, fate_names = _build_fates()
        # Annotate every hand card with how many copies are left in the deck
        # so the deck_tracker.jsonl debug dump shows them inline. `remaining()`
        # is keyed by canonical name (handles paired transform cards).
        remaining_map = counter.remaining() if counter else {}
        for hc in hand:
            if isinstance(hc, dict):
                canon = _canonical(hc.get("name", ""))
                hc["copiesLeftInDeck"] = remaining_map.get(canon)
                # Canonical key into counter.remaining, so the counter window
                # can pin / filter the rows for cards currently in hand.
                hc["counterKey"] = canon
        vm["me"] = {
            "destiny": me.destiny, "hp": hp_val,
            "hpIsPredicted": hp_is_predicted,
            "hpFromLog": log_hp,
            "tipoFromLog": (log_tipo if log_tipo is not None else 0),
            "maxTipoFromLog": log_maxtp,
            "logFromRound": log_round,
            "xiuwei": xiuwei, "tipo": tipo_val, "max_tipo": max_tipo,
            "realm_tier": realm,
            "rerolls": rerolls,
            "unlocked": unlocked,
            "hand": hand, "board": board,
            "seasonal": seasonal,
            "fates": fates, "fateNames": fate_names,
            "breakthrough": {"available": bool(avail), "threshold": thr, "gate": gate},
            # Slot indices where a 灵羽 lv2/3 found no eligible lv1 target. When
            # non-empty, the UI shows "未识别卡片 — 伤害计算不可用" instead of
            # damage numbers (yisim has no 灵羽 implementation).
            "lingyuUnresolved": lingyu_unresolved,
            # 灵植成长层数 → yisim *_stacks(归元草加血等),喂给伤害模拟。
            "plantStacks": _plant_stacks(getattr(me, "perm_buffs", None)),
        }

    # ── Opponent (matchup target) ──────────────────────────────────────────────
    # Round 14: use opp.board DIRECTLY — it's the current-round board, parsed
    # from f200.6 on each player struct in the latest GameStatus. The server
    # broadcasts every player's board at the start of each round; that's the
    # roster the opponent will fight us with this round (boards lock between
    # rounds). OpponentTracker still observes for history, but we don't pull
    # the displayed board from it anymore.
    opp = _pick_opponent(state)
    if opp is not None:
        board = [_card(c) for c in (opp.board or [])]
        from_round = state.round_num
        # Pad/truncate to 8 slots.
        board = (board + [None] * shadow_state.BOARD_SLOTS)[:shadow_state.BOARD_SLOTS]
        # R26: opponent fates decoded from f200["5"] (packed varint list, R24
        # Phase B addendum 8) flow through the SAME pipeline as me's fates —
        # `_fates_to_talents` builds talent objects with `phase = pick order`
        # so the matchup-mode sim can apply them via opts.opponentTalents.
        opp_fates, opp_fate_names = _fates_to_talents(getattr(opp, "fates", []) or [])
        # R28: BattleLog override for opponent too (same precedence as ME).
        opp_log = _battle_log_stats(state.round_num, getattr(opp, "display_name", ""))
        opp_log_hp    = int(opp_log["max_hp"])     if opp_log else 0
        opp_log_tipo  = int(opp_log["tipo"])       if opp_log else None
        opp_log_maxtp = int(opp_log["max_tipo"])   if opp_log else 0
        # Opponent HP: prefer BattleLog, fall back to formula prediction.
        # The wire doesn't carry HP for any player. When the formula is used,
        # `hpIsPredicted` is True so the UI can render the chip differently.
        opp_hp_predicted = (opp_log_hp <= 0)
        if opp_hp_predicted:
            opp_hp_val = _predict_hp(state.round_num, opp.realm_tier)
        else:
            opp_hp_val = opp_log_hp
        opp_tipo_val = opp_log_tipo if opp_log_tipo is not None else opp.tipo
        vm["opponent"] = {
            "player_id": opp.player_id,
            "character": "",  # opponents' class isn't directly exposed
            "destiny": opp.destiny, "hp": opp_hp_val,
            "hpIsPredicted": opp_hp_predicted,
            "hpFromLog": opp_log_hp,
            "tipoFromLog": (opp_log_tipo if opp_log_tipo is not None else 0),
            "maxTipoFromLog": opp_log_maxtp,
            "xiuwei": opp.xiuwei, "tipo": opp_tipo_val, "max_tipo": opp_log_maxtp,
            "realm_tier": opp.realm_tier,
            "phase": state.phase,
            "unlocked": unlocked,
            "board": board,
            "boardFromRound": from_round,
            "fates": opp_fates, "fateNames": opp_fate_names,
        }

    # R27: one-shot per-frame console probe so the user can read both
    # candidates without opening devtools. Prints ME plus every visible
    # player at GameStatus arrival — gated to `prep` to avoid duplicate
    # log lines during the battle phase.
    if state.phase == "prep":
        _hp_probe(state)

    return vm


_hp_probe_seen: set = set()


def _hp_probe(state) -> None:
    """Print per-player `(xiuwei, hp_field)` plus the BattleLog HP (the only
    authoritative source) once per (round, me-uid) pair. Diagnostic only.
    """
    me_uid = ""
    if 0 <= state.me_index < len(state.players):
        me_uid = state.players[state.me_index].player_id
    key = (state.round_num, me_uid)
    if key in _hp_probe_seen:
        return
    _hp_probe_seen.add(key)
    import sys
    for p in state.players:
        tag = "ME " if p.player_id == me_uid else "   "
        f5 = int(getattr(p, "hp_field", 0) or 0)
        log = _battle_log_stats(state.round_num, getattr(p, "display_name", ""))
        log_hp = int(log["max_hp"]) if log else 0
        print(f"[hp-probe] R{state.round_num} {tag}{p.player_id[:24]:24s} "
              f"xiuwei={p.xiuwei:3d} field5={f5:3d}  "
              f"hp(log)={log_hp:3d}",
              file=sys.stderr, flush=True)



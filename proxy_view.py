# Fate id → yi-sim talent metadata (generated by build_fate_map.py).
_FATE_MAP_PATH = Path(__file__).resolve().parent / "proxy" / "fate_talent_map.json"
_fate_map_cache = None
_unmapped_warned: set = set()


def _fate_map() -> dict:
    global _fate_map_cache
    if _fate_map_cache is None:
        try:
            _fate_map_cache = json.loads(_FATE_MAP_PATH.read_text(encoding="utf-8"))
        except Exception:
            _fate_map_cache = {}
    return _fate_map_cache


def _resolve_fate_meta(fmap: dict, fid: int, phase: int):
    """Look up a fate's talent metadata, picking the right phase variant.

    Backward-compat: today every fate_talent_map.json entry is single-variant
    (phase-agnostic). If a future entry adds `phases: {"1": {...}, "2": {...}}`,
    we merge the matching phase's overrides over the base entry. If the entry
    has no `phases` key the base entry is returned unchanged.
    Returns None when the id is unmapped.
    """
    base = fmap.get(str(fid))
    if not base:
        return None
    phases = base.get("phases") if isinstance(base, dict) else None
    if not isinstance(phases, dict):
        return base
    override = phases.get(str(phase)) or phases.get(int(phase) if isinstance(phase, int) else None)
    if isinstance(override, dict):
        merged = dict(base)
        merged.update(override)
        # `phases` itself isn't useful downstream — drop it.
        merged.pop("phases", None)
        return merged
    return base


def _warn_unmapped_fate(fid: int) -> None:
    """Log a one-shot warning (per process) when a fate id is missing from the
    talent map. The fate still surfaces in `fateNames` for display — we just
    can't model it in the damage sim yet."""
    if fid in _unmapped_warned:
        return
    _unmapped_warned.add(fid)
    try:
        name = shadow_state.fate_name(fid)
    except Exception:
        name = "?"
    print(f"[fate] unmapped id={fid} ({name}) — add to fate_talent_map.json to model in sim",
          file=sys.stderr, flush=True)


def _fates_to_talents(fate_ids):
    """Convert a list of fate ids (in PICK ORDER) into (talents, names).

    Each entry's index in the list = phase (1-indexed). The phase is stored
    both as the existing `position` field (yisim uses it to resolve any
    `{n}` placeholder in `runtimeKey`) AND as a new `phase` field on the
    talent object so the UI and any future phase-aware lookup can use it.
    Unmapped ids still surface in `names` but are skipped in `talents`,
    and a one-shot console warning fires per unique unmapped id.

    talents match the shape yisim.simulate expects via normalizeTalents:
      {detected, position, phase, name, simulationKind, runtimeKey,
       grantedCardBaseIds}

    Phase variants seam: if a future fate_talent_map.json entry adds
    `phases: { "2": { ... }, "3": { ... } }`, `_resolve_fate_meta` will
    apply the right override automatically — no caller change needed.
    """
    fmap = _fate_map()
    talents, names = [], []
    for i, fid in enumerate(fate_ids or []):
        try:
            fid_i = int(fid)
        except (TypeError, ValueError):
            continue
        phase = i + 1
        names.append(shadow_state.fate_name(fid_i))
        meta = _resolve_fate_meta(fmap, fid_i, phase)
        if not meta:
            _warn_unmapped_fate(fid_i)
            continue
        talents.append({
            "detected": True,
            "position": phase,
            "phase": phase,
            "name": meta.get("name", ""),
            "simulationKind": meta.get("simulationKind", "non-combat-or-unsupported"),
            "runtimeKey": meta.get("runtimeKey"),
            "grantedCardBaseIds": meta.get("grantedCardBaseIds") or [],
        })
    return talents, names


def _build_fates():
    """Back-compat shim: ME's fates from addon.chosen_fates → (talents, names)."""
    try:
        import addon
        ids = list(addon.chosen_fates)
    except Exception:
        ids = []
    return _fates_to_talents(ids)


def _card(c):
    """ZoneCard | CardState | None → {name, level} | None."""
    if c is None:
        return None
    return {"name": getattr(c, "name", "?"), "level": int(getattr(c, "level", 1) or 1)}


def _board_list(board, unlocked):
    out = []
    for i in range(shadow_state.BOARD_SLOTS):
        c = board[i] if board and i < len(board) else None
        out.append(_card(c))
    return out


class OpponentTracker:
    """Remembers each player's board per round so the matchup can use the
    opponent's board from a PREVIOUS round — in-game you never see an
    opponent's current-round board, only what they last fought with.
    """

    def __init__(self):
        self.reset()

    def reset(self):
        self._by_round = {}   # round_num -> {player_id: [card-dict|None, ...]}

    def observe(self, state):
        r = state.round_num
        slot = self._by_round.setdefault(r, {})
        for p in state.players:
            slot[p.player_id] = [_card(c) for c in (p.board or [])]
        # Keep memory bounded.
        if len(self._by_round) > 4:
            for old in sorted(self._by_round)[:-4]:
                self._by_round.pop(old, None)

    def last_seen(self, player_id, current_round):
        """The opponent's board from the most recent round before this one.
        Returns (board, from_round) or (None, None)."""
        rounds = sorted(rr for rr in self._by_round if rr < current_round)
        for rr in reversed(rounds):
            board = self._by_round[rr].get(player_id)
            if board and any(board):
                return board, rr
        return None, None

    def previous_round(self, player_id, current_round):
        """The opponent's board strictly from `current_round - 1`.

        Use this for the matchup panel — "what the current opponent had last
        round" — instead of `last_seen`, which can drift many rounds back if
        the opponent has been quiet. Falls back to `last_seen` if the previous
        round is missing (e.g. very first round we observed them).
        """
        slot = self._by_round.get(current_round - 1, {})
        board = slot.get(player_id)
        if board and any(board):
            return board, current_round - 1
        return self.last_seen(player_id, current_round)


def _pick_opponent(state):
    """Choose the opponent to display/simulate against.

    Preference order:
      1. **The next-round opponent** — `state.players[me].next_opponent_id` (the
         UID broadcast on the user's player struct, field 9). YiXianPai pairs you
         with a DIFFERENT opponent each round, so this is what the user is about
         to fight; not the same as the previously-fought opponent.
      2. The player the user most recently fought (`addon.my_last_opponent`)
         — fallback for round 1 / states where field 9 was missing.
      3. The live opponent with the most cards on board.
      4. The first non-me player.
    Returns a PlayerState or None.
    """
    if not state.players:
        return None
    me_idx = state.me_index
    others = [(i, p) for i, p in enumerate(state.players) if i != me_idx]
    if not others:
        return None

    # 1. Next-round matchup from the broadcast.
    next_uid = ""
    if 0 <= me_idx < len(state.players):
        next_uid = getattr(state.players[me_idx], "next_opponent_id", "") or ""
    if next_uid:
        for _, p in others:
            if p.player_id == next_uid:
                return p

    # 2. Fallback: most recently fought opponent (`my_last_opponent`).
    try:
        import addon
        target_uid = addon.my_last_opponent
    except Exception:
        target_uid = ""
    if target_uid:
        for _, p in others:
            if p.player_id == target_uid and p.destiny > 0:
                return p
    # Most board cards among still-alive opponents.
    alive = [(i, p) for i, p in others if p.destiny > 0] or others
    alive.sort(key=lambda ip: sum(1 for c in (ip[1].board or []) if c), reverse=True)
    return alive[0][1]


# ─── HP formula (predicted fallback when BattleLog.json is unavailable) ──────
#
# The wire doesn't carry HP. BattleLog.json is the authoritative source but
# only updates when the game flushes per-round entries, and isn't available
# at all for the current opponent until that round's battle is recorded.
# When BL is missing for a round we use this empirical formula:
#
#     HP = realm_base[realm] + (round_num - 1) * 2
#
# where realm_base = {1: 40, 2: 45, 3: 52, 4: 62, 5: 75} — the cumulative
# breakthrough HP totals (+5, +7, +10, +13 per breakthrough). The +2/round
# baseline grows with xiuwei progression. Verified exact-match for at least
# one full game; drifts under-count by ~0–10 HP late-game when the player
# picks max_hp-boost cards (锻髓, 大还丹, etc.) — those effects are not on
# the wire and would require simulating each card's per-turn rule.
#
# Always paired with a `predicted=True` flag so the UI can mark these values
# visually distinct from authoritative BL values.
_REALM_BASE_HP = {1: 40, 2: 45, 3: 52, 4: 62, 5: 75}


def _predict_hp(round_num: int, realm_tier: int) -> int:
    """Predict max_hp from realm + round. Returns 0 if inputs invalid."""
    if not (isinstance(round_num, int) and round_num >= 1):
        return 0
    base = _REALM_BASE_HP.get(realm_tier or 1)
    if base is None:
        return 0
    return base + (round_num - 1) * 2


def _battle_log_stats(round_num: int, display_name: str):
    """Look up `(life, xiuwei, tipo, max_tipo, max_hp, realm_tier,
    from_round)` from BattleLog.json for the given player display name.

    Returns None if the log isn't available, the module didn't import, the
    display name is missing, or no entry exists. The log is the only
    authoritative source for `max_hp`, `tipo`, and `max_tipo` — the wire
    protocol doesn't carry them directly. Callers should fall back to
    wire-derived values when this returns None.
    """
    if _battle_log is None or not display_name:
        return None
    try:
        return _battle_log.get_reader().extract_stats(round_num, display_name)
    except Exception:
        return None


